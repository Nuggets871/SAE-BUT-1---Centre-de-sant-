    private void jButtonCheminOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCheminOkActionPerformed
        // TODO add your handling code here:
        listModelListChemin.clear();
        if (jComboBoxChemin1.getItemCount()!=0){
            Sommet s1 = graphe.sommets.get(jComboBoxChemin1.getSelectedItem().toString());
            Sommet s2 = graphe.sommets.get(jComboBoxChemin2.getSelectedItem().toString());
            if (graphe.dijkstraDuree(s1, s2)!=null){
                if (jComboBoxChoix.getSelectedIndex()==0){
                List<Sommet> cheminPlusFiable = graphe.dijkstraFiabilite(s1, s2);
                for (Sommet sommet : cheminPlusFiable){
                    listModelListChemin.addElement("-> " +sommet.getNom());
                }
                }else if (jComboBoxChoix.getSelectedIndex()==1){

                    List<Sommet> cheminPlusCourtDistance = graphe.dijkstraDistance(s1, s2);
                    for (Sommet sommet : cheminPlusCourtDistance){
                        listModelListChemin.addElement("-> "+sommet.getNom());
                    }
                }else{

                    List<Sommet> cheminPlusCourtDuree = graphe.dijkstraDuree(s1, s2);
                    for (Sommet sommet : cheminPlusCourtDuree){
                        listModelListChemin.addElement("-> "+sommet.getNom());
                    }
                }
            }else {
                JOptionPane.showMessageDialog(this, "Ces centres ne sont pas reliés", "Erreur", JOptionPane.ERROR_MESSAGE);
            }
            
              
        }else {
            JOptionPane.showMessageDialog(this, "Aucun centre trouvé", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_jButtonCheminOkActionPerformed

    private void jButtonModifierOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModifierOkActionPerformed
        // TODO add your handling code here:
        if (jComboBoxModifier1.getItemCount()!=0){
            Sommet sommet1 = graphe.getSommets().get(jComboBoxModifier1.getSelectedItem().toString());
            Sommet sommet2 = graphe.getSommets().get(jComboBoxModifier2.getSelectedItem().toString());
            Lien lien = null;

            for (Lien l : graphe.getLiens()) {
                if ((l.getSommet1() == sommet1 && l.getSommet2() == sommet2) ||
                        (l.getSommet1() == sommet2 && l.getSommet2() == sommet1)) {
                    lien = l;
                    break;
                }
            }

            if (lien != null) {
                double duree = lien.getDureeMoyenne();
                double distance = lien.getDistance();
                double fiabilite = lien.getFiabilite();


                jTextFieldDuree.setText(String.valueOf(duree));
                jTextFieldDistance.setText(String.valueOf(distance));
                jTextFieldFiabilite.setText(String.valueOf(fiabilite));

                jTextFieldDuree.setEditable(true);
                jTextFieldDistance.setEditable(true);
                jTextFieldFiabilite.setEditable(true);
            } else {
                jTextFieldDuree.setText("");
                jTextFieldDistance.setText("");
                jTextFieldFiabilite.setText("");
            }



        }else JOptionPane.showMessageDialog(this, "Centre non trouvé", "Erreur", JOptionPane.ERROR_MESSAGE);
        
        
        
        
    }//GEN-LAST:event_jButtonModifierOkActionPerformed

