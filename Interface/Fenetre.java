    private void jButtonTrajetOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTrajetOkActionPerformed
        // TODO add your handling code here:
        try{
            double fiab = Double.parseDouble(jTextFieldTrajetFiabilite.getText());
            if (fiab<0 || fiab>100){
                JOptionPane.showMessageDialog(this, "La fiabilité doit être entre 0 et 100", "Erreur", JOptionPane.ERROR_MESSAGE);
            }
            fiab=fiab/10;
            listModelListTrajetRisque.clear();

            for (Lien l : graphe.getLiens()) {
                if (l.getFiabilite()>=fiab){
                    listModelListTrajetRisque.addElement(l.getSommet1().getNom()+" ("+l.getSommet1().getType()+      ") - "+l.getSommet2().getNom() + " (" + l.getSommet2().getType() +") | "+  graphe.getLienEntreCentres(l.getSommet1().getNom(),l.getSommet2().getNom()).getFiabilite()*10 +"%"     );
                }
            }
            
            
            
        }catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(this, "Les valeurs doivent être des doubles", "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        
        
        
        
    }//GEN-LAST:event_jButtonTrajetOkActionPerformed
    

    
