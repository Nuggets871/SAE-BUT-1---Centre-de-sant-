    private void jButtonConfirmerChangementActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmerChangementActionPerformed
        Sommet sommet1 = graphe.getSommets().get(jComboBoxModifier1.getSelectedItem().toString());
        Sommet sommet2 = graphe.getSommets().get(jComboBoxModifier2.getSelectedItem().toString());
        Lien lien = null;

        for (Lien l : graphe.getLiens()) { //Rechercher le lien sommet1/sommet2
            if ((l.getSommet1() == sommet1 && l.getSommet2() == sommet2) ||
                    (l.getSommet1() == sommet2 && l.getSommet2() == sommet1)) {
                lien = l;
                break;
            }
        }

        if (lien != null) {
            
            //Récuperer les infos des jTextField
            try {
                double duree = Double.parseDouble(jTextFieldDuree.getText());
                double distance = Double.parseDouble(jTextFieldDistance.getText());
                double fiabilite = Double.parseDouble(jTextFieldFiabilite.getText());
                //Mettre à jour le lien avec les nouvelles données
                if (fiabilite > 10) { //Parce que fiabilité de plus de 10 n'est pas autorisé
                    JOptionPane.showMessageDialog(jDialogModifierLien,"La fiabilité dois être compris entre 1 et 10 !");
                }else if (duree<0 ||distance<0||fiabilite<0){
                    JOptionPane.showMessageDialog(jDialogModifierLien,"Les valeurs doivent être positif !");
                            
                } else {
                    lien.setDureeMoyenne(duree);
                    lien.setDistance(distance);
                    lien.setFiabilite(fiabilite);
                    JOptionPane.showMessageDialog(jDialogModifierLien,"Les changements ont été pris en compte !");
                }
            } catch (NumberFormatException ex){
                        JOptionPane.showMessageDialog(this, "Les valeurs doivent être des doubles", "Erreur", JOptionPane.ERROR_MESSAGE);
                        }
            
        }
    }//GEN-LAST:event_jButtonConfirmerChangementActionPerformed

