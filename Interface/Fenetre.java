    private void jMenuChargerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuChargerActionPerformed
        JFileChooser fileChooser = new JFileChooser();
       
       String repertoireCourant = System.getProperty("user.dir");
       fileChooser.setCurrentDirectory(new File(repertoireCourant));

       int result = fileChooser.showOpenDialog(this);

       if (result == JFileChooser.APPROVE_OPTION) {
           File selectedFile = fileChooser.getSelectedFile();
           String nomFichier = selectedFile.getAbsolutePath();

           if (nomFichier.endsWith(".csv")) {
               // Créer une nouvelle instance de Graphe
               graphe = new Graphe();
               // Charger le graphe à partir du fichier
               graphe.chargerGraphe(nomFichier);
               jPanneauDessin = new PanneauDessin(graphe);
               this.setContentPane(jPanneauDessin);

               // Mettre à jour la fenêtre
               revalidate();
               repaint();
               JOptionPane.showMessageDialog(this, "Le graphe a été chargé avec succès", "Status chargement", JOptionPane.INFORMATION_MESSAGE);
           } else {
               JOptionPane.showMessageDialog(this, "Le fichier sélectionné n'est pas un fichier CSV", "Erreur", JOptionPane.ERROR_MESSAGE);
           }
       } else if (result == JFileChooser.CANCEL_OPTION) {
           
       }
        
    }//GEN-LAST:event_jMenuChargerActionPerformed

